/*
E1.1 Write a program that defines two variables,
the base, and height of a rectangle, and
calculates its area and the perimeter
*/

const base = 5;
const height = 12;

const area = base * height;
const perimeter = 2 * (base + height);

console.log(`Area: ${area}`);
console.log(`Perimeter: ${perimeter}`);
